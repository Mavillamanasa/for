var day = "Wednesday";

switch (day) {
    case "Monday":
        console.log("It's the start of the work week. Let's be productive!");
        break;
    case "Tuesday":
        console.log("It's Tuesday! Keep pushing through.");
        break;
    case "Wednesday":
        console.log("Midweek! You're halfway there.");
        break;
    case "Thursday":
        console.log("Almost Friday! Stay focused.");
        break;
    case "Friday":
        console.log("It's Friday! Finish strong.");
        break;
    case "Saturday":
        console.log("Weekend vibes! Time to relax.");
        break;
    case "Sunday":
        console.log("Rest up and recharge for the week ahead.");
        break;
    default:
        console.log("Not a valid day.");
}

